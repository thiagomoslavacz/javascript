// while
var c = 1;
while (c <= 6) {
  console.log(`Passo ${c}`);
  c++;
}

console.log("------------");

// do while
var c = 1;
do {
  console.log(`Passo ${c}`);
  c++;
} while (c <= 6);

// for
for (var c = 1; c <= 6; c++) {
  console.log(c);
}

